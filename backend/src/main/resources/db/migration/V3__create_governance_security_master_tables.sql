CREATE INDEX idx_roles_tenant_id ON roles (tenant_id);
CREATE INDEX idx_permissions_tenant_id ON permissions (tenant_id);
CREATE INDEX idx_company_profile_types_tenant_id ON company_profile_types (tenant_id);
